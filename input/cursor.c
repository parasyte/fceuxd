void FCEU_DrawCursor(uint8 *buf, int xc, int yc)
{
 int x,y;
 int c,d;

  if(xc<256 && yc<240)
  for(y=0;y<19;y++)
   for(x=0;x<11;x++)
   {
    uint8 a;
    a=FCEUcursor[y*11+x];
    if(a)
    {
     c=(yc+y);
     d=(xc+x);
     if(d<256 && c<240)
      buf[c*272+d]=a+127;
    }
   }
}
